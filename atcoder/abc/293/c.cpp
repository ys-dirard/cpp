int H, W;
int A[19][19];
int di[2] = {0, 1};
int dj[2] = {1, 0};


int dfs(int x, int y, set<int> st){
    // cout << x << " " << y << endl;
    // cout <<"st: ";
    // for(auto itr=st.begin();itr!=st.end();itr++){
    //     cout << *itr << " ";
    // }
    // cout << endl;
    if(x==H && y==W) return 1;

    int ans = 0;
    set<int> st2 = st;
    for(int i=0;i<2;i++){
        int nx = x+di[i];
        int ny = y+dj[i];
        
        if(nx<=H && ny<=W && st.count(A[nx][ny])==0){
            st2.insert(A[nx][ny]);
            ans += dfs(nx, ny, st2);
            st2.erase(A[nx][ny]);
        }
    }
    return ans;
}

