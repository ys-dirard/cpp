#include <bits/stdc++.h>
using namespace std;

int main(){
    // sin/cos/tan ///////////////////////////////////////////////////////////////////
    double pi = 3.141592653589793238;
    double x = 30;
    cout << "sin / cos / tan" << endl;
    printf("%.12lf\n", sin(x / 180.0 * pi));
    printf("%.12lf\n", cos(x / 180.0 * pi));
    printf("%.12lf\n", tan(x / 180.0 * pi));
    return 0;
}